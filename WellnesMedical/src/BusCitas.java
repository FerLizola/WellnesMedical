    private void btnCancelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelActionPerformed
       TDACita f=new TDACita();
       if(!f.eliminar(tabla)){
           javax.swing.JOptionPane.showMessageDialog(this, "Seleccione una cita");
       } else {
       f.limpiarTabla(tabla);
       Mostrar();
       }
    }//GEN-LAST:event_btnCancelActionPerformed

    private void btnCambioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCambioActionPerformed
      TDACita t=new TDACita();
      int i=tabla.getSelectedRow();
        if(i==-1){
            javax.swing.JOptionPane.showMessageDialog(this, "Seleccione una Cita");
        }
        else {
            String rf=tabla.getValueAt(i, 3).toString();
            t.eliminar(tabla);
            Cambio g=new Cambio(NSS, rf);
            g.setVisible(true);
            t.limpiarTabla(tabla);
            btnCambio.setEnabled(false);
            btnCancel.setEnabled(false);
            btnRefresh.setEnabled(true);
        }
      
    }//GEN-LAST:event_btnCambioActionPerformed

    private void btnRefreshActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRefreshActionPerformed
        Mostrar();
        btnCambio.setEnabled(true);
        btnCancel.setEnabled(true);
        btnRefresh.setEnabled(false);
    }//GEN-LAST:event_btnRefreshActionPerformed
    
    public void Mostrar(){
        TDACita t=new TDACita();
        t.mostrar(modelo, NSS);
        t.Nombre(NSS);
        txtNombre.setText(t.paciente);
        txtNSS.setText(NSS);
    }
    
    
