    private void btnMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarActionPerformed
    TDAResultadoAnalisis a=new TDAResultadoAnalisis();
    String NSS=txtPaciente.getText();
    if(NSS.isEmpty()){
        javax.swing.JOptionPane.showMessageDialog(null, "Ingrese un NSS de paciente");
    } else {
        if(a.mostrarReceta(NSS)){
            txtAnalisis.setText(a.getAna_nom());
            txtHoraFecha.setText(a.getFecha()+"  "+a.getHora());
            txtResultados.setText(a.getResultado());
            cbxEstado.setSelectedItem(a.getEstado());
            btnEstado.setEnabled(true);
            txtPaciente.setEditable(false);
        } else {
            javax.swing.JOptionPane.showMessageDialog(null, "No existe resultado de analisis para el paciente ingresado");
        }
    }
    }//GEN-LAST:event_btnMostrarActionPerformed

    private void btnEstadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEstadoActionPerformed
    TDAResultadoAnalisis a=new TDAResultadoAnalisis();
    String est=cbxEstado.getSelectedItem().toString();
    String ns=txtPaciente.getText();
    if(a.ActualizarEstado(est, ns)){
        javax.swing.JOptionPane.showMessageDialog(null, "Estado de Revision Modificado"); 
        txtPaciente.setEditable(true);
        btnEstado.setEnabled(false);
    }else {
        javax.swing.JOptionPane.showMessageDialog(null, "No ha sido posible Modificar el estado de Revision"); 
    }
    }//GEN-LAST:event_btnEstadoActionPerformed

