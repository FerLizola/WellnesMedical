    private void btnGuardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnGuardarMouseClicked
        if(txtNom.getText().isEmpty()||txtDesc.getText().isEmpty()||txtCod.getText().isEmpty()||
                txtPiezas.getText().isEmpty()||txtStock.getText().isEmpty()||txtFecha.getText().isEmpty()){
            showMessageDialog(null,"¡Rellene todos los campos para guardar datos!");
        }
        else{
            MedicamentoTDA med=new MedicamentoTDA(txtNom.getText(),txtDesc.getText(),
                    (String)(cbxTipo.getSelectedItem()),(String)(cbxPres.getSelectedItem()),
                    Integer.parseInt(txtPiezas.getText()),Integer.parseInt(txtStock.getText()),
                    Long.parseLong(txtCod.getText()),txtFecha.getText());
            if(med.guardar()){
                txtNom.setText("");
                txtDesc.setText("");
                cbxTipo.setSelectedIndex(0);
                cbxPres.setSelectedIndex(0);
                txtPiezas.setText("");
                txtStock.setText("");
                txtCod.setText("");
                txtFecha.setText("");
                showMessageDialog(null,"Registro Exitoso, Medicamento Agregado a Inventario");
            }
        }
    }//GEN-LAST:event_btnGuardarMouseClicked

